export async function manyGPX(files: FileList) {
  const arrs = [];
	const db = await initDB();
  for (let i = 0; i < files.length; i++) {
    const file = files.item(i)!;
    const arr = await fromGPX(file);
    if (arr) {
      addData(file.name, arr, db);
      arrs.push(arr);
    }
  }
  const finalArr = new Int32Array(arrs.reduce((l, a) => l + a.length, 0));
  let i = 0;
  for (const a of arrs) for (let j = 0; j < a.length; j++) finalArr[i++] = a[j];
  return finalArr;
}

